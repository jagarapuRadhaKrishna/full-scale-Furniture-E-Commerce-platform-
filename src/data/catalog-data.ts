// Utility functions
export const getAllProducts = () => {
  const products = [];
  DFWCatalog.categories.forEach(category => {
    category.subcategories.forEach(subcategory => {
      products.push(...subcategory.products);
    });
  });
  return products;
};

export const getProductsByCategory = (categoryId: string) => {
  const category = DFWCatalog.categories.find(c => c.id === categoryId);
  if (!category) return [];
  
  const products = [];
  category.subcategories.forEach(subcategory => {
    products.push(...subcategory.products);
  });
  return products;
};

export const getProductsBySubcategory = (categoryId: string, subcategoryId: string) => {
  const category = DFWCatalog.categories.find(c => c.id === categoryId);
  if (!category) return [];
  
  const subcategory = category.subcategories.find(s => s.id === subcategoryId);
  return subcategory ? subcategory.products : [];
};

export const getProductByModel = (modelCode: string) => {
  const allProducts = getAllProducts();
  return allProducts.find(product => product.model === modelCode);
};

export const getCategories = () => {
  return DFWCatalog.categories.map(({ id, name }) => ({ id, name }));
};

export const getSubcategories = (categoryId: string) => {
  const category = DFWCatalog.categories.find(c => c.id === categoryId);
  return category ? category.subcategories.map(({ id, name }) => ({ id, name })) : [];
};